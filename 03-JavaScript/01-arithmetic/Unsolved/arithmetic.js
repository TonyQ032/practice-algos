// Super simple adding function. Returns the sum of num1 and num2
const add = (num1, num2) => {
  return num1 + num2;
};

// Super simple subtraction function. Returns the difference of num1 and num2
const subtract = (num1, num2) => {
  return num1 - num2;
};

// Super simple multiplication function. Returns the product of num1 and num2
const multiply = (num1, num2) => {
  return num1 * num2;
};

// Super simple division function. Returns the quotient of num1 and num2
const divide = (num1, num2) => {
  return num1 / num2;
};
